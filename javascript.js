//Calculator functions

function addition(a, b) {
    return a + b;
}

function substraction(a, b) {
    return a - b;
}

function multiplication (a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}

function pow(a, b) {
    return Math.pow(a, b);
}

function sqrt(a) {
    return Math.sqrt(a);
}